        public void actualizarReporte(){
        CtlCliente controladorCliente = new CtlCliente();
        ArrayList<Cantidad_Mascotas_Por_Cliente> reporteNumMascotas = controladorCliente.ArrayCantidadMascotas();
        DefaultPieDataset dataset = new DefaultPieDataset();
        for (Cantidad_Mascotas_Por_Cliente reporteCliente : reporteNumMascotas) {
            dataset.setValue(reporteCliente.getNombreCliente(), reporteCliente.getCantidadMacotas());
        }
        JFreeChart chart = ChartFactory.createPieChart("Mascotas por Cliente", dataset, true, true, Locale.ITALY);
        ChartPanel panel = new ChartPanel(chart);
        panel.setMouseWheelEnabled(true);
        jPanelReporte.setLayout(new java.awt.BorderLayout());
        jPanelReporte.add(panel,BorderLayout.CENTER);
        jPanelReporte.validate();
    }
    
    


