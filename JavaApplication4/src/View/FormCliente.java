    public void ActualizarListaCliente() {
        try {
            // Actualizar cliente
            CtlCliente controladorCliente = new CtlCliente();
            ArrayList<Cliente> clientes = controladorCliente.ConsultarCliente();
            DefaultTableModel modeloCliente = new DefaultTableModel();
            modeloCliente.addColumn("Identificacion cliente");
            modeloCliente.addColumn("Nombre");
            modeloCliente.addColumn("Apellidos");
            modeloCliente.addColumn("Direccion");
            modeloCliente.addColumn("Telefono");
            Object[] datoCliente = new Object[5];
            for (Cliente cliente : clientes) {
                datoCliente[0] = cliente.getIdentificacion_cliente();
                datoCliente[1] = cliente.getNombre();
                datoCliente[2] = cliente.getApellidos();
                datoCliente[3] = cliente.getDireccion();
                datoCliente[4] = cliente.getTelefono();
                modeloCliente.addRow(datoCliente);
            }
            jTableClientes.setModel(modeloCliente);

        } catch (Exception e) {
        }
    }

